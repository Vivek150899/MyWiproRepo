
public class IntLiterals {

	public static void main(String[] args) {
		
		//Any value which is directly hard coded is called Literal Value
        System.out.println(50);//Decimal integer literal
        System.out.println(057);//Octal  integer literal 0 to 7
        System.out.println(0xF5); //Hexa Decimal Integer Value  a to f
        System.out.println(0b111);//Binary Integer Value
        
       
	}

}
